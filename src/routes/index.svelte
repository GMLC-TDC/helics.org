    <h1 class="mt-4">About</h1>
    <p>
      Our flexible and scalable open-source co-simulation framework is designed to integrate
      simulators designed for separate TDC domains to simulate regional and interconnection-scale
      power system behaviors at unprecedented levels of detail and speed. The target is to scale up
      to linking a 50,000-node transmission system with millions of distribution nodes, coupled with
      100,000 communication points. This simulation should enable planning studies in a turnaround
      time of minutes to hours, instead of days with today's simulation technologies, a speedup of
      50 to 100 times â€” a feat not previously done before.
    </p>
    <p>
      This comprehensive TDC simulation tool is fundamental for investment decision-making by
      industry. It's also important to help quantify the impact of the ever-increasing high
      penetration variable generation on power grid reliability and resiliency.
    </p>
